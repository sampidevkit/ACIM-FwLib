void fastsincos(sin_cos_t *SinCosCxt, float Angle) // <editor-fold defaultstate="collapsed" desc="Fast sine, cosine">
{
    // Since we are using "float", it is not possible to get an index of array
    // directly. Almost every time, we will need to do interpolation, as per
    // following equation: - y = y0 + (y1 - y0)*((x - x0)/(x1 - x0))

    uint32_t y0_Index;
    uint32_t y0_IndexNext;
    float x0, x1, y0, y1, temp;

    if(Angle>=PI2_NUM)
    {
        int n=(int) (Angle/PI2_NUM);
        Angle-=(float) (n)*PI2_NUM;
    }

    if(Angle<0)
        Angle=Angle+PI2_NUM;
    y0_Index=(uint32_t) (Angle/SIN_COS_STEP);

    if(y0_Index>=4096)
    {
        y0_Index=0;
        y0_IndexNext=1;
        x0=PI2_NUM;
        x1=SIN_COS_STEP;
        temp=0;
    }
    else
    {
        y0_IndexNext=y0_Index+1;
        if(y0_IndexNext>=4096)
        {
            y0_IndexNext=0;
            x1=PI2_NUM;
        }
        else
            x1=((y0_IndexNext)*SIN_COS_STEP);
        x0=(y0_Index*SIN_COS_STEP);
        temp=((Angle-x0)/(x1-x0));
    }

    y0=SINE_TABLE[y0_Index];
    y1=SINE_TABLE[y0_IndexNext];
    SinCosCxt->Sin=y0+((y1-y0)*temp);

    y0=COSINE_TABLE[y0_Index];
    y1=COSINE_TABLE[y0_IndexNext];
    SinCosCxt->Cos=y0+((y1-y0)*temp);
}// </editor-fold>

float fastsin(float Angle) // <editor-fold defaultstate="collapsed" desc="Fast sine">
{
    sin_cos_t SinCosCxt;

    fastsincos(&SinCosCxt, Angle);

    return SinCosCxt.Sin;
}// </editor-fold>

float fastcos(float Angle) // <editor-fold defaultstate="collapsed" desc="Fast cosine">
{
    sin_cos_t SinCosCxt;

    fastsincos(&SinCosCxt, Angle);

    return SinCosCxt.Cos;
}// </editor-fold>

float fastatan2(float y, float x) // <editor-fold defaultstate="collapsed" desc="Fast arctang">
{
    float arctan2, z;

    if(x==0.0f)
    {
        if(y>0.0f)
            return PI_DIV_2;
        if(y==0.0f)
            return 0.0f;

        return -PI_DIV_2;
    }

    z=y/x;
    if(fabs(z)<1.0f)
    {
        arctan2=z/(1.0f+0.28f*z*z);
        if(x<0.0f)
        {
            if(y<0.0f)
                return (arctan2-PI_NUM);

            return (arctan2+PI_NUM);
        }
    }
    else
    {
        arctan2=PI_DIV_2-z/(z*z+0.28f);
        if(y<0.0f)
            return (arctan2-PI_NUM);
    }

    return arctan2;
} // </editor-fold>

float Runge_Kutta_Integration(float y_dot, float last_y, float T) // <editor-fold defaultstate="collapsed" desc="Runge Kutta Intergration">
{
    float k1=y_dot;
    float k2=y_dot+T/2*k1;
    float k3=y_dot+T/2*k2;
    float k4=y_dot+T*k3;

    return (last_y+T/6*(k1+2*k2+2*k3+k4));
} // </editor-fold>